const maps = { zh, en }

export function t(lang: Lang, key: keyof typeof zh, params?: Record<string, string | number>): string {
  let text = maps[lang]?.[key] ?? maps.zh[key] ?? key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, String(v))
    }
  }
  return text
}
